#include "Select.h"
#include "input.h"

#include "fade.h"

#include "bg.h"

//====================================================================================
//
//				コンストラクタ
//
//====================================================================================
Select::Select() {

}

//====================================================================================
//
//				デストラクタ
//
//====================================================================================
Select::~Select() {

}

//====================================================================================
//
//				初期化
//
//====================================================================================
void Select::Init() {
	// 背景初期化
	pBG = std::make_unique<BG>();
	pBG->Init();

}

//====================================================================================
//
//				終了
//
//====================================================================================
void Select::Uninit() {
	pBG->Uninit();
	pBG.reset();
}

//====================================================================================
//
//				更新
//
//====================================================================================
void Select::Update() {
	if (GetMouseRelease(MOUSEBUTTON_L) || GetKeyRelease(VK_SPACE)) {
		Fade::StartFadeOut(SCENE_GAME);

	}
}


//====================================================================================
//
//				描画
//
//====================================================================================
void Select::Draw() {

	pBG->Draw();
}