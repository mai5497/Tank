#include "EnemyManager.h"


//====================================================================================
//
//				コンストラクタ
//
//====================================================================================
EnemyManager::EnemyManager() {
}

//====================================================================================
//
//				デストラクタ
//
//====================================================================================
EnemyManager::~EnemyManager() {
}

//====================================================================================
//
//				初期化
//
//====================================================================================
void EnemyManager::Init() {
	enemyKillSum = 0;

	pEnemys = std::make_shared<Enemy[]>(MAX_ENEMY);
	for (int i = 0; i < MAX_ENEMY; i++) {
		pEnemys[i].Init();
	}
}

//====================================================================================
//
//				終了処理
//
//====================================================================================
void EnemyManager::Uninit() {
	pEnemys.reset();
}


//====================================================================================
//
//				更新
//
//====================================================================================
void EnemyManager::Update() {
	for (int i = 0; i < MAX_ENEMY; i++) {
		pEnemys[i].Update();
	}
}

//====================================================================================
//
//				描画
//
//====================================================================================
void EnemyManager::Draw() {
	for (int i = 0; i < MAX_ENEMY; i++) {
		pEnemys[i].Draw();
	}
}

//====================================================================================
//
//				撃破数取得
//
//====================================================================================
int EnemyManager::GetEnemyKillSum() {
	enemyKillSum = 0;
	for (int i = 0; i < MAX_ENEMY; i++) {
		if (pEnemys[i].GetUse() == true) {
			enemyKillSum++;
		}
	}

	return enemyKillSum;
}
